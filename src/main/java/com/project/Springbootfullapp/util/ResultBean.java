package com.project.Springbootfullapp.util;

/**
 * @author RAJA
 *
 */

public class ResultBean {
	private String result;

	/**
	 * @param result
	 */
	public ResultBean(String result) {
		super();
		this.result = result;
	}

	/**
	 * @return the result
	 */
	public String getResult() {
		return result;
	}

}
